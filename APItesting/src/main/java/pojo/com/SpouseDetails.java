package pojo.com;

public class SpouseDetails {

	String name;
	String email;
	int phno;
	
	public SpouseDetails(String name, String email, int phno) {
		super();
		this.name = name;
		this.email = email;
		this.phno = phno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getPhno() {
		return phno;
	}

	public void setPhno(int phno) {
		this.phno = phno;
	}
	
	
	
	
	
}
