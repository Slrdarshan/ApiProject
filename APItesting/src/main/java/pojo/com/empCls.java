package pojo.com;

public class empCls {
public String name;
public String empid;
public int phno;
public String emaid;

public empCls(String name, String empid, int phno, String emaid) {
	super();
	this.name = name;
	this.empid = empid;
	this.phno = phno;
	this.emaid = emaid;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public String getEmpid() {
	return empid;
}

public void setEmpid(String empid) {
	this.empid = empid;
}

public int getPhno() {
	return phno;
}

public void setPhno(int phno) {
	this.phno = phno;
}

public String getEmaid() {
	return emaid;
}

public void setEmaid(String emaid) {
	this.emaid = emaid;
}






}
